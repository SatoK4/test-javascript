// -----関数-----
// ・function
// 繰り返し使われる一連の処理を1つにまとめたもの。
// 関数の形で定義しておき、この関数を呼び出すことで、同じプログラムを書かずに処理を実行できる。
// 例）
// function 関数名(){
//   処理内容
// }

// ・引数（ひきすう）と戻り値
// 引数：入力する値
// 戻り値：出力される値

// 関数に引数を渡すときは、()内に引数を書く。
// 複数指定する場合は「,」でつなぐことができ、順番に引数が格納されていく。

// 関数の結果を出力したいときは、return内に戻り値を渡す。
// 例）
// function 関数名（引数）{
//   処理
//   return 戻り値;
// }